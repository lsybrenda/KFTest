package pojo;

public class Score {
	
	//姓名
	private String name;
	//第一项小分
	private String firsts;
	//第一项加权得分
	private String jqscore;
	//第二项小分
	private String seconds;
	//第三项小分
	private String thirds;
	//总分
	private String tatals;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getFirsts() {
		return firsts;
	}
	public void setFirsts(String firsts) {
		this.firsts = firsts;
	}
	public String getJqscore() {
		return jqscore;
	}
	public void setJqscore(String jqscore) {
		this.jqscore = jqscore;
	}
	public String getSeconds() {
		return seconds;
	}
	public void setSeconds(String seconds) {
		this.seconds = seconds;
	}
	public String getThirds() {
		return thirds;
	}
	public void setThirds(String thirds) {
		this.thirds = thirds;
	}
	public String getTatals() {
		return tatals;
	}
	public void setTatals(String tatals) {
		this.tatals = tatals;
	}
	
	

}
